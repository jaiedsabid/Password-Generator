    private void jButtonGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerateActionPerformed
        passGen = new PasswordGenerator();
        int passLength = 0;
        
        passLength = getSelectedPassLength();
        
        if(jbuttonGroup.getSelection().getActionCommand().equals("yes"))
        {
            passGen.generatetPasswordSpecial(passLength);
            jTextFieldPassword.setText(passGen.getGeneratedPassword());
        }
        
        else
        {
            passGen.generatetPasswordNormal(passLength);
            jTextFieldPassword.setText(passGen.getGeneratedPassword());
        }
    }//GEN-LAST:event_jButtonGenerateActionPerformed


    public int getSelectedPassLength()
    {
        if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 0)
        {
            return 8;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 1)
        {
            return 9;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 2)
        {
            return 10;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 3)
        {
            return 11;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 4)
        {
            return 12;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 5)
        {
            return 13;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 6)
        {
            return 14;
        }
        
        else if(Integer.parseInt(String.valueOf(jComboBoxLength.getSelectedIndex())) == 7)
        {
            return 15;
        }
        
        else{return 16;}
    }
    
    public void copyToClipboardButton()
    {
        String generatedPassword = "";
        
        generatedPassword = jTextFieldPassword.getText();
        
        Toolkit toolKit = Toolkit.getDefaultToolkit();
        Clipboard clipBoard = toolKit.getSystemClipboard();
        StringSelection strSel = new StringSelection(generatedPassword);
        clipBoard.setContents(strSel, null);
    }

